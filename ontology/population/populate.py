"""
Ontology Population Pipeline for SENSO Framework — Paper 1

Populates the Software Evolution Ontology (SEO) from GitHub repository data.
For a given repository, this pipeline:
  1. Mines git history via PyDriller (commits, authors, files, churn)
  2. Computes cyclomatic complexity via lizard on monthly snapshots
  3. Parses package manifests (requirements.txt, package.json, pom.xml)
  4. Maps extracted data to SEO ontology classes via JSON-LD intermediate format
  5. Computes derived properties (phase labels, law conformance, agent adoption)
  6. Loads triples into Apache Jena Fuseki via SPARQL INSERT
  7. Runs HermiT consistency check via Owlready2

Used by: Paper 1 (ontology population, RQ2 accuracy evaluation)

Usage:
    python populate.py --repo owner/name --fuseki-url http://localhost:3030 --dataset seo
    python populate.py --repos-file repos.json --fuseki-url http://localhost:3030 --dataset seo
"""

